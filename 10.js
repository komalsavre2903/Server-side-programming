function isNull(value) {
    return value === null;
}

// Example usage:
var val1 = null;
var val2 = "Hello";
var val3 = 123;
var val4 = undefined;

console.log("Is val1 null? " + isNull(val1));
console.log("Is val2 null? " + isNull(val2));
console.log("Is val3 null? " + isNull(val3));
console.log("Is val4 null? " + isNull(val4));
